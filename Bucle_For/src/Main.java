public class Main {
    public static void main(String[] args) {

//        Para el bucle For, crea una variable numeroFor, esta variable tendrá como
//        valor 0 y su condición será que la variable sea igual
//        o menor que 3, se irá incrementando en 1 su valor cada vez que se ejecute y deberá mostrarse por pantalla.

        for (int contador = -10; contador <= 3; contador = contador +1 ){
            System.out.println(contador);
        }

    }
}